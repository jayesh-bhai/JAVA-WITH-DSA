public class PascalsTrianglesSum {
    public static void main(String[] args) {
        
        int n = 4;
        System.out.println(1<<n);
        // In pascals triangle sum of row is equal to 2 ^ n where n is the no. of row
        // here, 1<<n means 2 ^ n
        // Therefore, by using left shit operator we can find sum of nth row of pascals triangle.
    }
}
